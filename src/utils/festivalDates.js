// Function to check if today is a festival
export const getCurrentFestival = () => {
  const today = new Date();
  const currentMonth = today.getMonth() + 1;
  const currentDay = today.getDate();

  // Create a date object for comparison
  const currentDate = new Date(
    today.getFullYear(),
    today.getMonth(),
    today.getDate(),
  );

  // Find if today matches any festival (single or multi-day)
  const festival = festivals.find((f) => {
    if (f.duration > 1) {
      // For multi-day festivals, check if current date is within range
      const festivalStart = new Date(today.getFullYear(), f.month - 1, f.day);
      const festivalEnd = new Date(
        today.getFullYear(),
        f.month - 1,
        f.day + f.duration - 1,
      );
      return currentDate >= festivalStart && currentDate <= festivalEnd;
    } else {
      // Single day festival
      return f.month === currentMonth && f.day === currentDay;
    }
  });

  return festival || null;
};

// Function to get festival for a specific date (useful for testing)
export const getFestivalForDate = (date) => {
  const month = date.getMonth() + 1;
  const day = date.getDate();

  return festivals.find((f) => f.month === month && f.day === day) || null;
};
